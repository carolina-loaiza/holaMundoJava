  @P_MONTO			NUMERIC(300),
  @P_TASA			DECIMAL(10,3),
  @P_CUOTA			DECIMAL(10,3),
  @P_FECHA_INICIO DATE,
  @P_ESTADO   BOOLEAN,
  @P_SALDO_OPERACION	NUMERIC(300)
AS
