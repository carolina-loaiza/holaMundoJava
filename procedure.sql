CREATE PROCEDURE [dbo].[CREATE_CLIENTES_PR]
	@P_CEDULA	VARCHAR(6),
  @P_NOMBRE			VARCHAR(30),
  @P_APELLIDO		VARCHAR(30),
  @P_GENERO			VARCHAR(1),
  @P_EDAD			NUMERIC(3),
  @P_FECHA_NACIMIENTO DATE,
  @P_ESTADO_CIVIL VARCHAR(1)
AS
	INSERT INTO [dbo].[CLIENTES] VALUES(@P_CEDULA,@P_NOMBRE,@P_APELLIDO,@P_GENERO,@P_EDAD,@P_FECHA_NACIMIENTO,@P_ESTADO_CIVIL);
GO;

CREATE PROCEDURE [dbo].[RETRIEVE_ALL_CLIENTES_PR]
AS
	SELECT CEDULA.CLIENTES, NOMBRE.CLIENTES, APELLIDO.CLIENTES, GENERO.CLIENTES, EDAD.CLIENTES, PROVINCIA.DIRECCIONES, CANTON.DIRECCIONES, DISTRITO.DIRECCIONES
  FROM CLIENTES, DIRECCIONES
  WHERE CEDULA.CLIENTES = ID_CLIENTE.DIRECCIONES;
RETURN 0;

CREATE PROCEDURE [dbo].[RETRIEVE_BY_ID_CLIENTES_PR] @ID_CLIENTE VARCHAR(6)
AS
	SELECT CEDULA.CLIENTES, NOMBRE.CLIENTES, APELLIDO.CLIENTES, GENERO.CLIENTES, EDAD.CLIENTES, PROVINCIA.DIRECCIONES, CANTON.DIRECCIONES, DISTRITO.DIRECCIONES
  FROM CLIENTES, DIRECCIONES
  WHERE CEDULA.CLIENTES = ID_CLIENTE.DIRECCIONES AND CEDULA.CLIENTES = @ID_CLIENTE;
RETURN 0;

CREATE PROCEDURE [dbo].[DELETE_BY_ID_CLIENTES_PR] @ID_CLIENTE VARCHAR(6)
AS
	DELETE FROM CLIENTES
  WHERE CEDULA = @ID_CLIENTE;
RETURN 0;


CREATE PROCEDURE [dbo].[CREATE_CUENTAS_PR]
	@P_ID_CUENTA	VARCHAR(6),
